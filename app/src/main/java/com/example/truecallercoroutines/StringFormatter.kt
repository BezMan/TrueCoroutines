object StringFormatter {

    internal fun show15thChar(htmlStr: String): CharSequence {
        return htmlStr.subSequence(0, 20)
    }

    internal fun show15thCharsArray(htmlStr: String): CharSequence {
        return htmlStr.subSequence(0, 20)
    }

    internal fun showCountPerWord(htmlStr: String): CharSequence {
        return htmlStr.subSequence(0, 20)
    }


}